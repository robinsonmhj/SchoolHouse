	function display(data) {
		//console.log(data);
		var errorDiv=$("#error");
		errorDiv.empty();
		if(data.msg=="no data returned"){
			$("#googleMap").hide();
			$("#error").show();
			errorDiv.append("no data");
		}else{
			var collegeInfo=data.result.CollegeInfo;
			var houseInfo=data.result.HouseInfo;
			$("#error").hide();
			$("#googleMap").show();
			myLatlng=new google.maps.LatLng(collegeInfo.lat,collegeInfo.lng)
			var marker = new google.maps.Marker({
			    position: myLatlng,
			    title:collegeInfo.name
			});
			
			/*
			map.addListener('center_changed', function() {
		          // 3 seconds after the center of the map has changed, pan back to the
		          // marker.
		          window.setTimeout(function() {
		            map.panTo(marker.getPosition());
		          }, 3000);
		        });
		  */
			deleteMarkers();
			map.setCenter(myLatlng);
			map.setZoom(15);
			marker.setMap(map);
			markers.push(marker);
			var image="http://maps.google.com/mapfiles/ms/icons/blue-dot.png";
			for(i=0;i<houseInfo.length;i++){
				//console.log(houseInfo[i]);
				var geoinfo=houseInfo[i][0];
				var other=houseInfo[i][1];
				myLatlng=new google.maps.LatLng(geoinfo.lat,geoinfo.lng)
				marker = new google.maps.Marker({
				    position: myLatlng,
				    price:other.id,
				    label:(i+1).toString(),
				    title:other.price.toString(),
				    icon: {
				        url: image,
				        //scaledSize: new google.maps.Size(20, 20)
				    }
				});
				marker.setMap(map);
				markers.push(marker);
				/*
				markers[i+1].addListener('click', function() {
			          console.log("my id is "+markers[i+1].label);
			        });
			        */
				google.maps.event.addListener(markers[i+1], 'click', function() {
				    //alert(this.price);
				    window.open("postDetail?pid="+this.price);
				});
				
			}
			
			
		}
		
	}
	
